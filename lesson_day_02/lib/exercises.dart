class Horse{

  int age;
  String color;

  Horse(this.age, this.color);
}


void main() {

  Horse Tsagaan =  Horse(1, 'Tsagaan');
  print(Tsagaan.age);
  print(Tsagaan.color);
  print('===========');

  Horse Ulaan =  Horse(1, 'Ulaan');
  print(Ulaan.age);
  print(Ulaan.color);
  print('===========');

  Horse Shar =  Horse(1, 'Shar');
  print(Shar.age);
  print(Shar.color);
  print('===========');
}